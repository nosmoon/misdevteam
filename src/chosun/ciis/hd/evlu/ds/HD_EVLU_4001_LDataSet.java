	public String errcode;
	public String errmsg;

	public HD_EVLU_4001_LDataSet(){}
	public HD_EVLU_4001_LDataSet(String errcode, String errmsg){
		this.errcode = errcode;
		this.errmsg = errmsg;
	}

	public void setErrcode(String errcode){
		this.errcode = errcode;
	}

	public void setErrmsg(String errmsg){
		this.errmsg = errmsg;
	}

	public String getErrcode(){
		return this.errcode;
	}

	public String getErrmsg(){
		return this.errmsg;
	}

	public void getValues(CallableStatement cstmt) throws SQLException{
		this.errcode = Util.checkString(cstmt.getString(1));
		this.errmsg = Util.checkString(cstmt.getString(2));
		if(!"".equals(this.errcode)){
			return;
		}

		ResultSet rset0 = (ResultSet) cstmt.getObject(7);
		while(rset0.next()){
			HD_EVLU_4001_LCURLISTRecord rec = new HD_EVLU_4001_LCURLISTRecord();
			rec.use_chk = Util.checkString(rset0.getString("use_chk"));
			rec.frdt = Util.checkString(rset0.getString("frdt"));
			rec.todt = Util.checkString(rset0.getString("todt"));
			rec.evlu_obj_bgn_dd = Util.checkString(rset0.getString("evlu_obj_bgn_dd"));
			rec.evlu_obj_end_dd = Util.checkString(rset0.getString("evlu_obj_end_dd"));
			this.curlist.add(rec);
		}
	}
