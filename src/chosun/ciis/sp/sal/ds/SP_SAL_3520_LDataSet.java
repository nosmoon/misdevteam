	public void getValues(CallableStatement cstmt) throws SQLException{
		this.errcode = Util.checkString(cstmt.getString(1));
		this.errmsg = Util.checkString(cstmt.getString(2));
		if(!"".equals(this.errcode)){
			return;
		}

		ResultSet rset0 = (ResultSet) cstmt.getObject(6);
		while(rset0.next()){
			SP_SAL_3520_LCURLISTRecord rec = new SP_SAL_3520_LCURLISTRecord();
			rec.date_client_req = Util.checkString(rset0.getString("date_client_req"));
			rec.recipient_net = Util.checkString(rset0.getString("recipient_net"));
			rec.recipient_num = Util.checkString(rset0.getString("recipient_num"));
			rec.rslt_name = Util.checkString(rset0.getString("rslt_name"));
			this.curlist.add(rec);
		}
	}
