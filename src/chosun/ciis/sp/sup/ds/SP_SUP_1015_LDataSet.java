	public ArrayList curlist1 = new ArrayList();
	public String errcode;
	public String errmsg;

	public SP_SUP_1015_LDataSet(){}
	public SP_SUP_1015_LDataSet(String errcode, String errmsg){
		this.errcode = errcode;
		this.errmsg = errmsg;
	}

	public void setErrcode(String errcode){
		this.errcode = errcode;
	}

	public void setErrmsg(String errmsg){
		this.errmsg = errmsg;
	}

	public String getErrcode(){
		return this.errcode;
	}

	public String getErrmsg(){
		return this.errmsg;
	}

	public void getValues(CallableStatement cstmt) throws SQLException{
		this.errcode = Util.checkString(cstmt.getString(1));
		this.errmsg = Util.checkString(cstmt.getString(2));
		if(!"".equals(this.errcode)){
			return;
		}

		ResultSet rset0 = (ResultSet) cstmt.getObject(5);
		while(rset0.next()){
			SP_SUP_1015_LCURLIST1Record rec = new SP_SUP_1015_LCURLIST1Record();
			rec.step = Util.checkString(rset0.getString("step"));
			rec.cd = Util.checkString(rset0.getString("cd"));
			rec.cdnm = Util.checkString(rset0.getString("cdnm"));
			rec.up_cd = Util.checkString(rset0.getString("up_cd"));
			rec.sort = Util.checkString(rset0.getString("sort"));
			this.curlist1.add(rec);
		}
	}
