	public void getValues(CallableStatement cstmt) throws SQLException{
		this.errcode = Util.checkString(cstmt.getString(1));
		this.errmsg = Util.checkString(cstmt.getString(2));
		if(!"".equals(this.errcode)){
			return;
		}

		ResultSet rset5 = (ResultSet) cstmt.getObject(6);
		while(rset5.next()){
			MC_BUDG_2116_LCURLISTRecord rec = new MC_BUDG_2116_LCURLISTRecord();
			rec.cmpy_cd = Util.checkString(rset5.getString("cmpy_cd"));
			rec.dept_cd = Util.checkString(rset5.getString("dept_cd"));
			rec.budg_type_cd = Util.checkString(rset5.getString("budg_type_cd"));
			rec.budg_cd = Util.checkString(rset5.getString("budg_cd"));
			rec.budg_nm = Util.checkString(rset5.getString("budg_nm"));
			this.curlist.add(rec);
		}
	}
