	public ArrayList curlist = new ArrayList();
	public String errcode;
	public String errmsg;

	public EN_INNEXP_1504_LDataSet(){}
	public EN_INNEXP_1504_LDataSet(String errcode, String errmsg){
		this.errcode = errcode;
		this.errmsg = errmsg;
	}

	public void setErrcode(String errcode){
		this.errcode = errcode;
	}

	public void setErrmsg(String errmsg){
		this.errmsg = errmsg;
	}

	public String getErrcode(){
		return this.errcode;
	}

	public String getErrmsg(){
		return this.errmsg;
	}

	public void getValues(CallableStatement cstmt) throws SQLException{
		this.errcode = Util.checkString(cstmt.getString(1));
		this.errmsg = Util.checkString(cstmt.getString(2));
		if(!"".equals(this.errcode)){
			return;
		}

		ResultSet rset0 = (ResultSet) cstmt.getObject(9);
		while(rset0.next()){
			EN_INNEXP_1504_LCURLISTRecord rec = new EN_INNEXP_1504_LCURLISTRecord();
			rec.occr_no = Util.checkString(rset0.getString("occr_no"));
			rec.titl = Util.checkString(rset0.getString("titl"));
			rec.budg_cd = Util.checkString(rset0.getString("budg_cd"));
			rec.budg_nm = Util.checkString(rset0.getString("budg_nm"));
			rec.dr_amt = Util.checkString(rset0.getString("dr_amt"));
			rec.cr_amt = Util.checkString(rset0.getString("cr_amt"));
			this.curlist.add(rec);
		}
	}
