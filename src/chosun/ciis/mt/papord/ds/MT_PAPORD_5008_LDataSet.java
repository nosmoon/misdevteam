	public void getValues(CallableStatement cstmt) throws SQLException{
		this.errcode = Util.checkString(cstmt.getString(1));
		this.errmsg = Util.checkString(cstmt.getString(2));
		if(!"".equals(this.errcode)){
			return;
		}

		ResultSet rset0 = (ResultSet) cstmt.getObject(8);
		while(rset0.next()){
			MT_PAPORD_5008_LCURLISTRecord rec = new MT_PAPORD_5008_LCURLISTRecord();
			rec.fac_clsf = Util.checkString(rset0.getString("fac_clsf"));
			rec.a_roll = Util.checkString(rset0.getString("a_roll"));
			rec.c_roll = Util.checkString(rset0.getString("c_roll"));
			rec.b_roll = Util.checkString(rset0.getString("b_roll"));
			rec.car_cnt = Util.checkString(rset0.getString("car_cnt"));
			this.curlist.add(rec);
		}
	}
