	public void getValues(CallableStatement cstmt) throws SQLException{
		this.errcode = Util.checkString(cstmt.getString(1));
		this.errmsg = Util.checkString(cstmt.getString(2));
		if(!"".equals(this.errcode)){
			return;
		}

		ResultSet rset0 = (ResultSet) cstmt.getObject(4);
		while(rset0.next()){
			FC_FUNC_3070_LCURLISTRecord rec = new FC_FUNC_3070_LCURLISTRecord();
			rec.fund_cd = Util.checkString(rset0.getString("fund_cd"));
			rec.fund_cdnm = Util.checkString(rset0.getString("fund_cdnm"));
			rec.pay_condition = Util.checkString(rset0.getString("pay_condition"));
			rec.pay_amt = Util.checkString(rset0.getString("pay_amt"));
			this.curlist.add(rec);
		}
	}
