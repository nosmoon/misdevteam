	public void getValues(CallableStatement cstmt) throws SQLException{
		this.errcode = Util.checkString(cstmt.getString(1));
		this.errmsg = Util.checkString(cstmt.getString(2));
		if(!"".equals(this.errcode)){
			return;
		}

		ResultSet rset0 = (ResultSet) cstmt.getObject(7);
		while(rset0.next()){
			FC_ACCT_2213_LCURLISTRecord rec = new FC_ACCT_2213_LCURLISTRecord();
			rec.happen_slipno = Util.checkString(rset0.getString("happen_slipno"));
			rec.in_slipno = Util.checkString(rset0.getString("in_slipno"));
			rec.slip_rcpm_dt = Util.checkString(rset0.getString("slip_rcpm_dt"));
			rec.rcpm_dept_cd = Util.checkString(rset0.getString("rcpm_dept_cd"));
			rec.rcpm_amt = Util.checkString(rset0.getString("rcpm_amt"));
			this.curlist.add(rec);
		}
	}
